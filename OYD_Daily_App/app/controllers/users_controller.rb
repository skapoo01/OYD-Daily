class UsersController < ApplicationController
  before_action :set_user, only: [:show, :edit, :update, :destroy, :select_school]
  protect_from_forgery except: :select_school

  # GET /users
  # GET /users.json
  def index
    @users = User.all
  end

  # GET /users/1
  # GET /users/1.json
